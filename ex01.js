// 01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma,
// subtração, multiplicação e divisão desses valores.

function valores (valor1, valor2){
    console.log(`Soma dos valores:${valor1 + valor2}`)
    console.log(`Subtração dos valores:${valor1 - valor2}`)
    console.log(`Multiplição dos valores:${valor1 * valor2}`)
    console.log(`Divisão dos valores:${valor1 / valor2}`)
}

valores(4, 2)